#naredba ifelse

b = 19

if b < 15:
    print(f"Broj {b} je manji od 15.")
    print("Sada smo u bloku if.")
else:
    print(f"broj {b} nije manji od 15.")
    print("Sada smo u bloku else.")
print("Sada smo izvan bloka ifelse.")


